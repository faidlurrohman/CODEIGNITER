							<p style="text-align: justify !important;">Program wisata satu hari ke tiga gili dengan menggunakan glass bottom boat dengan mengelilingi gili trawangan, meno dan air bagi tamu yang tidak harus terjun ke laut untuk snorkling sudah bisa melihat keindahan bawah laut di semua area gili. penjemputan dimulai dari hotel tempat anda menginap.</p>
							<p style="text-align: justify !important;">Susunan program wisata tiga Gili menggunakan Glass Bottom Boat ke tempat Wisata snorkling terbaik di Lombok. Berikut ini daftar tempat-tempat wisata snorkling yang bisa anda kunjungi :</p>
                            <ol style="font: 400 14px/26px 'Roboto', sans-serif; color: #555555; text-align: justify; padding-bottom: 15px;">
                            	<b><li>Bukit Malimbu Satu</li></b>
                            	<p style="text-align: justify !important;">Sebelum anda sampai ke tempat snorkling yaitu tiga Gili, anda akan melewati sebuah bukit yang menawarkan keindahan pemandangan dari ketinggian. Bukit ini adalah Bukit Malimbu Satu. waktu yang diperlukan dari pusat Kota Mataram untuk bisa sampai di bukit ini adalah 1 jam saja dengan jarak tempuh 24 km. Banyak wisatawan yang mampir ke tempat ini untuk menikmati indahnya pemandangan yang tersaji di sini. Jadi sangat disayangkan jika anda akan snorkling ke tiga gili namun tidak mampir di tempat wisata yang satu ini. Apalagi ketika sore menjelang malam, anda bisa menyaksikan sunset terindah di tempat ini. Terlihat pula dari kejauhan tiga Gili yang berjejer. Bagi pecinta fotografi, inilah salah satu spot terbaik untuk mengambil banyak foto.</p>
                            	<b><li>Bukit Malimbu Dua</li></b>
                            	<p style="text-align: justify !important;">Bukit Malimbu Dua, terletak tidak jauh dari Bukit Malimbu Satu. Keduanya sama-sama masuk dalam kawasan Desa Malaka, Kecamatan Pemenang, Kabupaten Lombok Utara. Di tempat ini, anda juga disuguhkan dengan pemandangan yang tak kalah menarik. Terlihat dari bukit ini, teluk yang melengkung dengan pasir yang berwarna krem dipadu dengan air laut yang jernih berwarna biru. Sama dengan Bukit Malimbu Satu, di sini anda juga dapat menyaksikan matahari terbenam di sore hari.</p>
                            	<b><li>Gili Trawangan</li></b>
                            	<p style="text-align: justify !important;">Selanjutnya anda bisa menuju ke spotsnorkling pertama yaitu Gili Trawangan. Tempat wisata yang terletak di Desa Gili Indah Kecamatan Pemenanng Kabupaten Lombok Utara NTB ini sudah ramai dikunjungi wisatawan. Hal ini karena keindahannya yang sudah terkenal dan fasilitasnya sangat lengkap. Salah satu fasilitasnya yaitu tempat jasa snorkling maupun tempat penyewaan peralatan snorkling. Melalui jasa snorkling ini, anda sudah disediakan berbagai peralatan snorkling dan tentunya boat untuk mengantar anda ke spot terbaik untuk snorkling. Karena tidak semua wilayah disekitar gili bisa dijadikan spot untuk snorkling. Di bagian utara Gili Trawangan terdapat sebuah lokasi snorkling yang sangat keren. Lokasi ini dinamakan Coral Garden. Anda akan disuguhkan dengan pemandangan koral-koral yang berwarna-warni dengan berbagai jenis ikan yang menarik lainnya. Spot yang satu ini cukup dangkal sehingga bisa terlihat jelas semua makhluk hidup yang ada di dalamnya.</p>
                            	<b><li>Gili Meno</li></b>
                            	<p style="text-align: justify !important;">Setelah puas menikmati keindahan bawah laut Gili Trawangan, lokasi selanjutnya yaitu Gili Meno. Di Gili Meno ini juga terdapat lokasi terbaik untuk snorkling yaitu Meno wall. Letaknya berada di sisi barat Gili Meno dengan kedalaman 18 meter. Disanaanda akan menemui sebuah palung laut dengan berbagai biota laut yang mengagumkan. Spot lain yang bisa digunakan untuk menyelam antara lain Sea Point Penyu, PointCoralBlue dll.</p>
                            	<b><li>Gili Air</li></b>
                            	<p style="text-align: justify !important;">Gili Air juga menawarkan spot terbaik untuk snorkling. Lokasi yang biasanya digunakan snorkling memiliki kedalaman 1,5 meter hingga 3 meter saja. Di Gili Air ini terdapat sebuah lokasi snorkling yang sudah terkenal bernama BountyWrek. Disana terdapat sebuah bangkai kapal yang sudah lama karam. Sehingga ditumbuhi oleh terumbu-terumbu karang yang cantik. Snorkling di Gili Air cukup aman karena sudah dipasang tali pembatas sebagai batas aman snorkling.</p>
